package com.gxuwz.dorm.bean.entity;

public class SysDorm {

	private String dormId;
	private String dormsID;
	private int checkIn;
	private int rest;
	public String getDormId() {
		return dormId;
	}
	public void setDormId(String dormId) {
		this.dormId = dormId;
	}
	public String getDormsID() {
		return dormsID;
	}
	public void setDormsID(String dormsID) {
		this.dormsID = dormsID;
	}
	public int getCheckIn() {
		return checkIn;
	}
	public void setCheckIn(int checkIn) {
		this.checkIn = checkIn;
	}
	public int getRest() {
		return rest;
	}
	public void setRest(int rest) {
		this.rest = rest;
	}
	
	
}
